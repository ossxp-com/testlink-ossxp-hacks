<h3>总体测试计划度量</h3>
<p>本页面通过测试套件，拥有者和关键字来显示测试计划的当前状态。
'当前状态'由最近构建中测试用例的执行结果来决定。例如：当一个测试用例在多个构建中都执行过时，只考虑最终测试结果。</p>

<p>'最终测试结果'的概念在许多报告里使用. 它是这样定义的:</p>
<ul>
<li>构建加入到测试计划里的先后顺序决定了哪个构建是最新的。最新构建中的测试结果比旧构建的测试结果优先。例如：如果你在构建 1 里记录了一个测试用例的测试结果是’失败’。在构建2里记录同一个测试用例的测试结果是'通过',则最终的测试结果是'通过'。</li>
<li>如果同一个测试用例在同一个构建里执行了多次，那么最后一次执行的结果优先。 例如： 如果构建 3 分配给你的团队而且 tester 1 在下午2:00时标记为'通过',而 tester 2 在 下午3:00时标记为为'失败'，那么最终测试结果为'失败'。</li>
<li>那些显示'未执行'的测试用例不会纳入构建的测试结果中。例如：如果你在构建 1 中标记某一测试用例为'通过', 而在构建 2 中该测试用例还尚未执行，那么最终测试结果为'通过'。
</li>
</ul>
<p>以下显示的列表:</p>
<ul>
	<li><b>按顶级测试套件显示结果</b>
	表中列出顶级的测试套件的测试用例数目, 未执行数目，通过数目, 失败数目, 阻塞数目和完成度。完成度指通过，失败和阻塞的用例数量与总用例数的百分比。</li>
	<li><b>按关键字显示测试结果</b>
	表中列出当前测试计划中所有测试用例里的关键字, 以及对应的测试结果。</li>
	<li><b>按测试者显示测试结果</b>
